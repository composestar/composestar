package Composestar.RuntimeCore.FLIRT.Annotations;

import java.lang.annotation.ElementType;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

/**
 * A custom filter type with this annotation will get a special (i.e.
 * non-continue) filter action for the Reject-Call action.
 * 
 * @author Michiel Hendriks
 */
@Target(ElementType.TYPE)
@Retention(RetentionPolicy.RUNTIME)
public @interface FilterActionRejectCall
{
	EFlowBehavior flow() default EFlowBehavior.Continue;

	EMessageChangeBehavior mcb() default EMessageChangeBehavior.Original;

	String operations() default "";
}
